const plus = (firstNum, secondNum) => {
    return firstNum + secondNum;
}

const minus = (firstNum, secondNum) => {
    return firstNum - secondNum;
}

const multiple = (firstNum, secondNum) => {
    return firstNum * secondNum;
}

const divide = (firstNum, secondNum) => {
    return firstNum / secondNum;
}

module.exports = {
    plus,
    minus,
    multiple,
    divide,
}